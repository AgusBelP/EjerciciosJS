/* 8) Realizá un programa que permita al usuario ingresar dos números enteros. La computadora debe
mostrar los resultados de las 4 operaciones matemáticas básicas con tales números. */

num_1 = parseInt(prompt("Por favor ingrese el primer número entero"))
num_2 = parseInt(prompt("Por favor ingrese el segundo número entero"))

console.log(`El resultado de la operación suma es ${num_1+num_2}`)
console.log(`El resultado de la operación resta es ${num_1-num_2}`)
console.log(`El resultado de la operación multiplicación es ${num_1*num_2}`)
console.log(`El resultado de la operación suma es ${num_1/num_2}`)
